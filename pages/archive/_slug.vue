      <h1>{{ article.title }}</h1>
      <p>{{ article.description }}</p>
      <!-- <img :src="article.img" :alt="article.alt" /> -->
      <p>Article last updated: {{ formatDate(article.updatedAt) }}</p>

      <nuxt-content :document="article" />
      <!-- <category :category="article.category" /> -->
